package ders15_overloading_WhileLoop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        // 2 basmaklı sayıları aralarında bir boşluk olacak sekilde yanyana yazdırın

        for (int i = 10; i <=99 ; i++) {
            System.out.print(i+" ");
        }

        // While loop ile yapalım
        System.out.println("");
        int a=10;
        while (a<100){
            System.out.print(a+" ");
            a++;
        }



    }
}
